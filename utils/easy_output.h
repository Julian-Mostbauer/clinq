#ifndef EASY_OUTPUT_H
#define EASY_OUTPUT_H

/// @brief Prints every element in the integer array
/// @note Format: n1, n2, n3\n
/// @param arr The array to print.
/// @param len The length of the array.
void print_arr_i(int *arr, int len);

/// @brief Prints every element in the float array
/// @note Format: n1, n2, n3\n
/// @param arr The array to print.
/// @param len The length of the array.
void print_arr_f(float *arr, int len);

#endif // EASY_OUTPUT_H